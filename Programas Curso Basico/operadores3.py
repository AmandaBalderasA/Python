# Programa que calcula operadores
num1 = int(input("Ingrese el primer numero: "))
num2 = int(input("Ingrese el segundo numero: "))

# Operacion == 
print("La comparacion == es: ", num1 == num2)

# Operacion != 
print("La comparacion != es: ", num1 != num2)

# Operacion <
print("La comparacion < es: ", num1 < num2)

# Operacion >
print("La comparacion > es: ", num1 > num2)

# Operacion <= 
print("La comparacion <= es: ", num1 <= num2)

# Operacion >=
print("La comparacion >= es: ", num1 >= num2)